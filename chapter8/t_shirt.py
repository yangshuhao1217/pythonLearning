def make_shirt(size, text='I love Python'):
    """Display the size of T-shirt and what will be printed on it."""
    print(f"Your T-shirt's size is {size}, '{text}' will be printed on it.")

make_shirt('L')
make_shirt("Large")
make_shirt('Medium')
make_shirt(size = 'Small', text = 'Show me the code') 
